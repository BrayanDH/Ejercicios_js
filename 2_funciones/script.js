// funciones

function suma(a, b) {
  return a + b;
}

console.log(suma(1, 4));

// funcion resta

function resta(a, b) {
  return a - b;
}

console.log(resta(3, 2));

// funcion multiplicacion

function multiplicacion(a, b) {
  return a * b;
}

console.log(multiplicacion(3, 2));

// funcion division

function division(a, b) {
  return a / b;
}

console.log(division(4, 2));
